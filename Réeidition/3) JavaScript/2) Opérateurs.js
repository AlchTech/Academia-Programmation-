// Les Opérateurs
    // +       // Addition
    // -       // Soustraction
    // *       // Multiplication
    // /       // Division
    // %       // Modulo, c'est le reste de la division
    // **      // Puissance, exemple 5**2 = 5 + 5 + 5 + 5 + 5 = 5*5
    
let nombreUn = 8, nombreDeux = 4, result;  //result est une variable vide sans valeur actuellement
result = nombreUn + nombreDeux;
console.log(result);        // 12
console.log(result + 2);    // 14

// Racourci
result += 2;    // Ajout rapide :  Permet d'ajouter 2 ou un autre nombre
result ++;      // Incremation : Permet d'ajouter 1 au nombre du resultat
result --;      // Décrementation : Permet d'enlever 1 au nombre du resultat